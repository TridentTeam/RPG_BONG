package jp.trident.game.fw;

public class Vector2 {

	/**
	 * X座標
	 */
	public float x = 0.0f;

	/**
	 * Y座標
	 */
	public float y = 0.0f;



	/**
	 * コンストラクタ
	 */
	public Vector2() {
		this.x = 0.0f;
		this.y = 0.0f;
	}

	/**
	 * コンストラクタ
	 */
	public Vector2(float x, float y) {
		this.x = x;
		this.y = y;
	}

}
